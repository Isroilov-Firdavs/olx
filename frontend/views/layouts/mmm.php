            <div class="container px-lg-5">
                <a class="navbar-brand" href="/"><i class="bi bi-clipboard-check"></i> E'lonlar doskasi</a>
                <button class="navbar-toggler" type="button" data-bs-toggle="collapse" data-bs-target="#navbarSupportedContent" aria-controls="navbarSupportedContent" aria-expanded="false" aria-label="Toggle navigation"><span class="navbar-toggler-icon"></span></button>
                <div class="collapse navbar-collapse" id="navbarSupportedContent">
                    <ul class="navbar-nav ms-auto mb-2 mb-lg-0">
                        <?php
                        if (!Yii::$app->user->isGuest) {
                            echo "<li class='nav-item'><a class='nav-link' href=".Url::to(['site/profile', 'id'=> Yii::$app->user->id]).">Mening e'lonlarim</a></li>";
                        }
                        ?>
                        <?php if (Yii::$app->user->isGuest) {?>
                        <li class="nav-item"><a class="nav-link" href="<?=Url::to(['site/signup'])?>">Registratsiya</a></li>
                        <?php }?>
                        <?php
                        if (Yii::$app->user->isGuest) {
                            echo "<li class='nav-item'><a class='nav-link' href=".Url::to(['site/login']).">Login</a></li>";
                        } else {
                            echo Html::beginForm(['/site/logout'], 'post', ['class' => 'd-flex'])
                                . Html::submitButton(
                                    'Logout (' . Yii::$app->user->identity->username . ')',
                                    ['class' => 'btn btn-link logout text-decoration-none']
                                )
                                . Html::endForm();
                        }
                        ?>
                    </ul>
                </div>
            </div>
        </nav>